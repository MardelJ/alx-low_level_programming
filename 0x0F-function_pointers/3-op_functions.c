/**
 * op_add - function that add 2 numbers
 *
 * @a: the first number
 * @b: the second number
 *
 * Return: the resut
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subtract 2 numbers
 *
 * @a: the first number
 * @b: the second number
 *
 * Return: the resut
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplies 2 numbers
 *
 * @a: the first number
 * @b: the second number
 *
 * Return: the resut
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides 2 numbers
 *
 * @a: the first number
 * @b: the second number
 *
 * Return: the resut
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that calculate the rest of division of 2 numbers
 *
 * @a: the first number
 * @b: the second number
 *
 * Return: the resut
 */
int op_mod(int a, int b)
{
	return (a % b);
}
